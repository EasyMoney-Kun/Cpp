#include<iostream>
using namespace std;
int main()
{
    // 换行符 \n
    cout<<"hello world!\n";
    cout<<"hello world!"<<endl;
    // 反斜杠 
    cout<<"\\"<<endl;
    // 水平制表符 \t 占8个字符的位置
    cout<<"hello\t world!"<<endl;
    system("pause");
    return 0;
}
/**************************************
作用：用于表示一些不能显示的字符
常用的转义字符：\n \\ \t
***************************************/