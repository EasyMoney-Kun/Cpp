#include <iostream>
using namespace std;
int main()
{
    int a = 10, b = 10;
    a += 10;
    b = b + 10;
    cout << "a=" << a << endl;
    cout << "b=" << b << endl;
    system("pause");
    return 0;
}
/*******************************
作用：用于将表达式的值赋给变量 += -= *= /= %=
********************************/